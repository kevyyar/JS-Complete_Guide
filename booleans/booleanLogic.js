-2 > 1; // false
10 < 9.99; // false
2 >= 2; // true
2 >= 5; // false
2 >= 1; // true

// == (equality) -> COMPARISONS (comparing value and not TYPE)
4 == 4; // true
"a" == "a"; // true
false == false; // true
"b" == "c"; // false
7 == "7"; // true

// != (not equal) -> COMPARISONS

// === (strict equality) -> COMPARISONS (compares BOTH VALUE and TYPE)
2 === "2"; // false
3 === 3; // true

// !== (strict non-equality) -> COMPARISONS
console.log(1 !== "1"); // true
